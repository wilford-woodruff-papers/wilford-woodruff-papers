<?php

namespace App\Nova;

use App\Nova\Actions\AssignRole;
use App\Nova\Actions\CopyUserRoles;
use App\Nova\Actions\ExportUsers;
use App\Nova\Filters\ApiUser;
use Illuminate\Http\Request;
use JeffersonSimaoGoncalves\NovaPermission\Nova\Fields\RoleBooleanGroup;
use Laravel\Nova\Fields\DateTime;
use Laravel\Nova\Fields\Gravatar;
use Laravel\Nova\Fields\ID;
use Laravel\Nova\Fields\Password;
use Laravel\Nova\Fields\Select;
use Laravel\Nova\Fields\Text;
use Laravel\Nova\Http\Requests\NovaRequest;

class User extends Resource
{
    public static $group = 'Admin';

    /**
     * The model the resource corresponds to.
     *
     * @var string
     */
    public static $model = \App\Models\User::class;

    public static $with = [
        'roles',
    ];

    /**
     * The single value that should be used to represent the resource when being displayed.
     *
     * @var string
     */
    public static $title = 'name';

    /**
     * The columns that should be searched.
     *
     * @var array
     */
    public static $search = [
        'id', 'name', 'email',
    ];

    public static function usesScout(): bool
    {
        return false;
    }

    /**
     * Get the fields displayed by the resource.
     */
    public function fields(NovaRequest $request): array
    {
        return [
            ID::make()->sortable(),

            Gravatar::make()->maxWidth(50),

            Text::make('Name')
                ->sortable()
                ->rules('required', 'max:255'),

            Text::make('Email')
                ->sortable()
                ->rules('required', 'email', 'max:254')
                ->creationRules('unique:users,email')
                ->updateRules('unique:users,email,{{resourceId}}'),

            Select::make('Timezone')
                ->options(array_combine(
                    \DateTimeZone::listIdentifiers(\DateTimeZone::ALL),
                    \DateTimeZone::listIdentifiers(\DateTimeZone::ALL)
                ))
                ->displayUsingLabels()
                ->hideFromIndex(),

            Password::make('Password')
                ->onlyOnForms()
                ->creationRules('required', 'string', 'min:8')
                ->updateRules('nullable', 'string', 'min:8'),

            DateTime::make('Last Login', 'last_login_at')
                ->hideWhenCreating()
                ->hideWhenUpdating(),
            //MorphToMany::make('Roles'),
            RoleBooleanGroup::make('Roles'),
        ];
    }

    /**
     * Get the cards available for the request.
     */
    public function cards(Request $request): array
    {
        return [];
    }

    /**
     * Get the filters available for the resource.
     */
    public function filters(Request $request): array
    {
        return [
            new ApiUser,
            new \App\Nova\Filters\Role,
        ];
    }

    /**
     * Get the lenses available for the resource.
     */
    public function lenses(Request $request): array
    {
        return [];
    }

    /**
     * Get the actions available for the resource.
     */
    public function actions(Request $request): array
    {
        return [
            new AssignRole,
            new CopyUserRoles,
            new ExportUsers,
        ];
    }
}
