                                    <p class="text-primary text-lg">Wilford Woodruff (1807-1898) traveled vast distances for decades to teach and testify of the restored gospel of Jesus Christ. He documented his ministry and his witness of the restoration in 31 daybooks and journals and more than 13,000 letters. In these records he also documented the teachings of Joseph Smith and other prophets and apostles. But today his records are found mostly in archives or in books known to a few experts.</p>

                                    <p class="text-primary text-lg">The Wilford Woodruff Papers Foundation exists to change that. It was organized in January 2020 to accomplish one objective: to make Wilford Woodruff&#39;s records of the restoration of the gospel of Jesus Christ accessible to everyone. Jennifer Ann Mackley extensively researched Wilford Woodruff's life and documents for over two decades before she and Donald W. Parry co-founded the Foundation and formed a team to accomplish this eternally significant endeavor.</p>

                                    <p class="text-primary text-lg">On Wilford Woodruff&#39;s 213th birthday, March 1, 2020, with support from the Church History Department of The Church of Jesus Christ of Latter-day Saints, the Foundation announced its mission in cooperation with the Wilford Woodruff Family Association. One year later, on March 1, 2021, the Foundation formally launched an open access website <a href="https://wilfordwoodruffpapers.org">wilfordwoodruffpapers.org</a> with the first group of transcribed documents.</p>

                                    <p class="text-primary text-lg">The Wilford Woodruff Papers Project is like The Joseph Smith Papers. Both present high-resolution images of a prophet&#39;s handwritten documents side-by-side with accurate transcriptions based on professional document editing standards and practices. The scope of the two projects, however, is dramatically different. Joseph Smith's papers extend from 1828 to 1844. Wilford Woodruff's records begin in 1828 and end at his death in 1898. The number of Joseph Smith&#39;s documents is comparatively small, approximately 2,500, due to the length of his ministry and the difference in record keeping in the early 19th century. Wilford Woodruff daybooks and journals contain more than 11,000 pages. He wrote more than 13,000 letters and received more than 17,000. All of his daybooks and journals and all of his extant letters, together with his sermons and his legal documents and family history records, will be included.</p>

